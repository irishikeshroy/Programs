public class mathLog10 {
  
    public static void main(String[] args) {
         
         //declaring variables
         double x = 100;
         double y = 505.5;
         double z = 90.7;
         double e = 0.0;
         
         //printing output for log10 method.
         System.out.println("Math.log10(" + x + ") = " + Math.log10(x));
         System.out.println("Math.log10(" + y + ") = " + Math.log10(y));
         System.out.println("Math.log10(" + z + ") = " + Math.log10(z));
         System.out.println("Math.log10(" + e + ") = " + Math.log10(e));
    }
   
}
