import java.lang.Math;

class cbrt {

	public static void main(String args[])
	{
	
		double d = 0.0;
		double e = -0.0;

		// Input Positive Zero
		// Output Positive Zero
		System.out.println(Math.cbrt(d));

		// Input Negative Zero
		// Output Negative Zero
		System.out.println(Math.cbrt(e));
	}
}
