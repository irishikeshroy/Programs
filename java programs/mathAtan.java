public class mathAtan {
    public static void main(String[] args) {
        
        //declaring negative variables
        double x = -50.0;
        double y = -75.0;
        double z = -55.0;
        double e = -0.0;
        
        //inpitting a negative value
        //outputing a negative value
        System.out.println("Math.atan(" + x + ") = " + Math.atan(x));
        System.out.println("Math.atan(" + y + ") = " + Math.atan(y));
        System.out.println("Math.atan(" + z + ") = " + Math.atan(z));
        System.out.println("Math.atan(" + e + ") = " + Math.atan(e));
    }
}
    
